package config;

/**
 *
 * @author joses
 */
public class ComboBoxItem {
    private int id;
    private String nombre;

    public ComboBoxItem(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return nombre; // Esto es lo que se mostrar√° en el JComboBox
    }

   
}


