    void listar() {
         String sql = "SELECT staff.id, staff.first_name, staff.last_name, staff.role_code, reff_staff_roles.role_name " +
                 "FROM staff " +
                 "JOIN reff_staff_roles ON reff_staff_roles.code = staff.role_code;";
    try {
        con = cn.getConnection();
        st = con.createStatement();
        rs = st.executeQuery(sql);
        Object[] staffData = new Object[5]; // Asegúrate de que coincida con la cantidad de columnas a mostrar
        model = (DefaultTableModel) TablaDeDatosStaff.getModel();
        while (rs.next()) {
            staffData[0] = rs.getInt("id");               // ID del staff
            staffData[1] = rs.getString("first_name");    // Nombre del staff
            staffData[2] = rs.getString("last_name");     // Apellido del staff
            
            // Concatenar role_code y role_name en el formato "codigo - nombre"
            int roleCode = rs.getInt("role_code");
            String roleName = rs.getString("role_name");
            staffData[3] = roleCode + " - " + roleName;  // Asignar valor combinado al modelo de la tabla

            model.addRow(staffData);  // Añadir fila al modelo
        }
        TablaDeDatosStaff.setModel(model);
    } catch (Exception e) {
        e.printStackTrace();
        }

    }

    void borrarRegistro() {
        if (!txtId.getText().equals("")) {
            BorrarConf bc = new BorrarConf(this.ventana, true);
            bc.setVisible(true);
            if (bc.getReturnStatus() == 1) {
                //
                int id = Integer.parseInt(txtId.getText());
                String sql = "DELETE FROM staff WHERE id=" + id;
                try {
                    con = cn.getConnection();
                    st = con.createStatement();
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Staff Deleted");
                    limpiarTexts();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error deleting record: " + e.getMessage(), "Delete record", JOptionPane.ERROR_MESSAGE);
                }
                //
            }
        } else {
            JOptionPane.showMessageDialog(null, "The id field is empty, to delete a record an id is required.\nPlease try again.", "Delete record", JOptionPane.ERROR_MESSAGE);
        }
        actualizar();
    }

    void modificarRegistro() {
        if (txtId.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "The id field is empty, to modify a record an id is required.\nPlease try again.", "Modify record", JOptionPane.ERROR_MESSAGE);
    } else {
        try {
            // solicitando valores
            int id = Integer.parseInt(txtId.getText());
            String lastName = txtLastName.getText();
            String firstName = txtFirstName.getText();
            int roleCode = Integer.parseInt(txtRoleName.getText()); // Se obtiene solo el código
            //sql
            String sql = "UPDATE public.staff SET first_name='" + firstName + "', last_name='" + lastName + "', role_code=" + roleCode + " WHERE id=" + id + ";";
            con = cn.getConnection();
            st = con.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "¡Registration modified successfully!", "Modify record", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error modifying record: " + e.getMessage(), "Modify record", JOptionPane.ERROR_MESSAGE);
        }
    }
    actualizar();
    }

    void limpiarTexts() {
        txtId.setText("");
        txtLastName.setText("");
        txtFirstName.setText("");
        txtRoleName.setText("");
    }

    void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i < TablaDeDatosStaff.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }

    void actualizar() {
        limpiarTabla(model);
        listar();
    }




