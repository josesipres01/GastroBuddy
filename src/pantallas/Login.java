    private void boton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton1ActionPerformed
// Obtener el usuario y contrase√±a ingresados
        String username = userTxf.getText().trim();
        String password = new String(passwordTxf.getPassword()).trim();

// Validar si los campos est√°n vac√≠os
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please complete both fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Detener el flujo si hay campos vac√≠os
        }

// Llamar al m√©todo de autenticaci√≥n
        if (Authentication.authUser(username, password)) {
            // Crear una instancia de la ventana "VentanaPrincipal"            
            new VentanaPrincipal().setVisible(true);

            // Cerrar el JFrame actual
            javax.swing.SwingUtilities.getWindowAncestor(this).dispose();
        } else {
            // Si la autenticaci√≥n falla, mostrar un mensaje de error
            JOptionPane.showMessageDialog(this, "Invalid username or password.", "Error", JOptionPane.ERROR_MESSAGE);

            // Limpiar los campos de usuario y contrase√±a
            userTxf.setText("");
            passwordTxf.setText("");
        }
   
    }//GEN-LAST:event_boton1ActionPerformed

