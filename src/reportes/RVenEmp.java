    public void generarRVenEmp() {
    try {
        Conexion cn = new Conexion();
        Connection con;
        Statement st;
        ResultSet rs;
        
        String idNomEmp = cBoxStaff.getSelectedItem().toString();
        String[] idEmp = idNomEmp.split("-");
        
        String sqlDatetime = jForDateofReport.getText();
        String anoMesDia[] = sqlDatetime.split("-");
        String archivo = "RVentasEmp-" + sqlDatetime + "-" + idNomEmp + ".html";

        FileWriter arch = new FileWriter(archivo);
        arch.write("<!DOCTYPE html>");
        arch.write("<html lang=\"en\">");
        arch.write("<head>");
        arch.write("<meta charset=\"UTF-8\">");
        arch.write("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">");
        arch.write("<title>Reporte de Meals</title>");
        arch.write("<style>");
        arch.write("body {font-family: Arial, sans-serif; margin: 0; padding: 0; background-color: #DEF7F4;}");
        arch.write(".container {max-width: 800px; margin: 20px auto; padding: 20px; background-color: #fafbfb; border-radius: 8px; box-shadow: 0 0 10px rgba(0, 0, 0, 0.1);}");
        arch.write("h1 {text-align: center; color: #333;}");
        arch.write("table {width: 100%; border-collapse: collapse; margin-bottom: 20px;}");
        arch.write("th, td {padding: 8px; text-align: left; border-bottom: 1px solid #ddd;}");
        arch.write("th {background-color: #f2f2f2;}");
        arch.write(".total {margin-top: 20px; text-align: right; font-weight: bold;}");
        arch.write("</style>");
        arch.write("</head>");
        arch.write("<body>");
        arch.write("<div class=\"container\">");
        arch.write("<h1>REPORTE DE VENTAS POR EMPLEADO</h1>");
        arch.write("<p>REPORTE DEL MES: " + sqlDatetime + "</p>");
        arch.write("<table>");
        arch.write("<thead>");
        arch.write("<tr>");
        arch.write("<th>staff_id</th>");
        arch.write("<th>first_name</th>");
        arch.write("<th>last_name</th>");
        arch.write("<th>date_of_meal</th>");
        arch.write("<th>cost_of_meal</th>");
        arch.write("</tr>");
        arch.write("</thead>");
        arch.write("<tbody>");
        
        if(cBoxStaff.getSelectedIndex() == 0){
            String sql = "SELECT staff.id, staff.first_name, staff.last_name, sales.date_of_meal, sales.amount FROM staff JOIN sales ON staff.id = sales.id_staff WHERE EXTRACT(MONTH FROM date_of_meal)=" + Integer.parseInt(anoMesDia[1]) + " AND EXTRACT(YEAR FROM date_of_meal)=" + Integer.parseInt(anoMesDia[0]) + " ORDER BY staff.id;";
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
        }else{
            String sql = "SELECT staff.id, staff.first_name, staff.last_name, sales.date_of_meal, sales.amount FROM staff JOIN sales ON staff.id = sales.id_staff WHERE staff.id="+ idEmp[0] +" AND EXTRACT(MONTH FROM date_of_meal)=" + Integer.parseInt(anoMesDia[1]) + " AND EXTRACT(YEAR FROM date_of_meal)=" + Integer.parseInt(anoMesDia[0]) +" ORDER BY staff.id;";
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
        }

        int cont = 0;
        double sum = 0;
        while (rs.next()) {
            arch.write("<tr>");
            arch.write("<td>" + rs.getString(1) + "</td>");
            arch.write("<td>" + rs.getString(2) + "</td>");
            arch.write("<td>" + rs.getString(3) + "</td>");
            arch.write("<td>" + rs.getString(4) + "</td>");
            arch.write("<td>$" + rs.getString(5) + "</td>");
            arch.write("</tr>");
            cont++;
            sum += Double.parseDouble(rs.getString(5));
        }

        arch.write("</tbody>");
        arch.write("</table>");
        arch.write("<p class=\"total\">TOTAL DE INGRESOS: $" + sum + "</p>");
        arch.write("<p class=\"total\">TOTAL DE VENTAS REALIZADAS: " + cont + "</p>");
        arch.write("</div>");
        arch.write("</body>");
        arch.write("</html>");
        arch.close();

        // Abre el archivo con el navegador predeterminado
        Runtime.getRuntime().exec("rundll32 url.dll,FileProtocolHandler " + archivo);

    } catch (Exception e) {
        System.out.println(e.getMessage());
    }
}
    
    public void generarStaff(){
        //
        try{
            Conexion cn = new Conexion();
            Connection con;
            Statement st;
            ResultSet rs;

            String sql = "SELECT id, first_name,last_name FROM staff ORDER BY id;";
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                cBoxStaff.addItem(rs.getString(1) + "-" + rs.getString(2) + "_" + rs.getString(3));
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
